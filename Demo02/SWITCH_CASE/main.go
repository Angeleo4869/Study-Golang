package main

//switch - case 结构
import "fmt"

func main() {
	finger := 3
	switch finger{
	case 1:{
		fmt.Println("Finger: ",finger)
	}
	case 2:{
		fmt.Println("Finger: ",finger)
	}
	case 3:{
		fmt.Println("Finger: ",finger)
	}
	case 4:{
		fmt.Println("Finger: ",finger)
	}
	case 5:{
		fmt.Println("Finger: ",finger)
	}
	default :{
		fmt.Println("Finger: ",finger)
	}
	}
}